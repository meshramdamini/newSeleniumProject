package LogicalProgram;

public class oddNumber {

	public static void main(String[] args) 
	{
		
		
		for(int i=1; i<=10; i++)
		{
			if(i%2==0)
			{
				System.out.println("number is even:"+i);
			}
			else
			{
				System.out.println("number is odd:"+i);
			}
		}
		
		

	}

}
