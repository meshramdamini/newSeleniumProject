package LogicalProgram;

public class largestString {

	public static void main(String[] args) 
	{
		String D = "Damini";
		
		String S = "SumedhBhagat";
		int count_d=D.length();
		int count_s=S.length();
		
		if (count_d > count_s)
		{
			System.out.println(D+ "d is greater and length : " +count_d);
		}
		else 
		{
			System.out.println(S+ "s is greater and length : " +count_s);
		}
      
       
       
 }
}
